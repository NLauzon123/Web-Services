	public ArrayList<StationInfo> radiusCalc(double lat, double lon, double rad) {
		HashMap<Integer, StationInfo> m = populateStationInfo(); 
		ArrayList<StationInfo> list = new ArrayList<StationInfo>();
		m.forEach((k, v) -> {
			double d = distanceCalc(lat, lon, v.getLat(), v.getLon(), 1);
			if (d <= rad) list.add(v);
		});
		return list;
	}
	public double distanceCalc(double lat1, double lon1, double lat2, double lon2, int type) {
		double latTop = 45.75;
		double latBot = 45.4;
		double lonTop = -73.3;
		double lonBot = -73.8;
		double eTop = 38889.814;
		double eBot = 39132.322;
		double northing = 38884.2715;
		double easting = eBot + (eTop - eBot) * (0.5 * (lat1 + lat2) - latBot) / (latTop - latBot);
		double north = northing * Math.abs(lat1 - lat2) / (latTop - latBot);
		double east = easting * Math.abs(lon1 - lon2) / (lonTop - lonBot);
		if (type == 0) return north + east; 
		return Math.sqrt(east * east + north * north);
	}
